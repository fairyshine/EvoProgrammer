}

evop_use_project_context_facts_dir() {
    local target_dir="$1"

    if [[ "$EVOP_PROJECT_CONTEXT_FACTS_DIR" != "$target_dir" ]]; then
        evop_reset_project_context_facts
        EVOP_PROJECT_CONTEXT_FACTS_DIR="$target_dir"
    fi
}

