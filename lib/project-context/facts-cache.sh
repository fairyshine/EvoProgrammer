    EVOP_PROJECT_CONTEXT_MAKEFILE_TARGETS_RESULT=""
    EVOP_PROJECT_CONTEXT_PACKAGE_JSON_SCRIPTS_RESULT=""
    EVOP_PROJECT_CONTEXT_WORKSPACE_MANIFESTS_RESULT=""
