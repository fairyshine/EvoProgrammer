evop_project_context_cache_lookup() {
    local cache_name="$1"
    local cache_key="$2"
    local found=0

    EVOP_PROJECT_CONTEXT_CACHE_LOOKUP_RESULT=""
    EVOP_PROJECT_CONTEXT_FACTS_CACHE_LOOKUPS=$((EVOP_PROJECT_CONTEXT_FACTS_CACHE_LOOKUPS + 1))

    case "$cache_name" in
        EVOP_PROJECT_CONTEXT_RELATIVE_EXISTS_CACHE)
            [[ -n ${EVOP_PROJECT_CONTEXT_RELATIVE_EXISTS_CACHE[$cache_key]+set} ]] || {
                EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES=$((EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES + 1))
                return 1
            }
            EVOP_PROJECT_CONTEXT_CACHE_LOOKUP_RESULT="${EVOP_PROJECT_CONTEXT_RELATIVE_EXISTS_CACHE[$cache_key]}"
            found=1
            ;;
        EVOP_PROJECT_CONTEXT_FILE_LITERAL_CACHE)
            [[ -n ${EVOP_PROJECT_CONTEXT_FILE_LITERAL_CACHE[$cache_key]+set} ]] || {
                EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES=$((EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES + 1))
                return 1
            }
            EVOP_PROJECT_CONTEXT_CACHE_LOOKUP_RESULT="${EVOP_PROJECT_CONTEXT_FILE_LITERAL_CACHE[$cache_key]}"
            found=1
            ;;
        EVOP_PROJECT_CONTEXT_FILE_REGEX_CACHE)
            [[ -n ${EVOP_PROJECT_CONTEXT_FILE_REGEX_CACHE[$cache_key]+set} ]] || {
                EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES=$((EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES + 1))
                return 1
            }
            EVOP_PROJECT_CONTEXT_CACHE_LOOKUP_RESULT="${EVOP_PROJECT_CONTEXT_FILE_REGEX_CACHE[$cache_key]}"
            found=1
            ;;
        EVOP_PROJECT_CONTEXT_FILE_TEXT_CACHE)
            [[ -n ${EVOP_PROJECT_CONTEXT_FILE_TEXT_CACHE[$cache_key]+set} ]] || {
                EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES=$((EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES + 1))
                return 1
            }
            EVOP_PROJECT_CONTEXT_CACHE_LOOKUP_RESULT="${EVOP_PROJECT_CONTEXT_FILE_TEXT_CACHE[$cache_key]}"
            found=1
            ;;
        EVOP_PROJECT_CONTEXT_MAKEFILE_TARGETS_CACHE)
            [[ -n ${EVOP_PROJECT_CONTEXT_MAKEFILE_TARGETS_CACHE[$cache_key]+set} ]] || {
                EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES=$((EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES + 1))
                return 1
            }
            EVOP_PROJECT_CONTEXT_CACHE_LOOKUP_RESULT="${EVOP_PROJECT_CONTEXT_MAKEFILE_TARGETS_CACHE[$cache_key]}"
            found=1
            ;;
        EVOP_PROJECT_CONTEXT_PACKAGE_JSON_SCRIPTS_CACHE)
            [[ -n ${EVOP_PROJECT_CONTEXT_PACKAGE_JSON_SCRIPTS_CACHE[$cache_key]+set} ]] || {
                EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES=$((EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES + 1))
                return 1
            }
            EVOP_PROJECT_CONTEXT_CACHE_LOOKUP_RESULT="${EVOP_PROJECT_CONTEXT_PACKAGE_JSON_SCRIPTS_CACHE[$cache_key]}"
            found=1
            ;;
        EVOP_PROJECT_CONTEXT_WORKSPACE_MANIFESTS_CACHE)
            [[ -n ${EVOP_PROJECT_CONTEXT_WORKSPACE_MANIFESTS_CACHE[$cache_key]+set} ]] || {
                EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES=$((EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES + 1))
                return 1
            }
            EVOP_PROJECT_CONTEXT_CACHE_LOOKUP_RESULT="${EVOP_PROJECT_CONTEXT_WORKSPACE_MANIFESTS_CACHE[$cache_key]}"
            found=1
            ;;
        EVOP_PROJECT_CONTEXT_WORKSPACE_PACKAGE_JSON_MANIFESTS_CACHE)
            [[ -n ${EVOP_PROJECT_CONTEXT_WORKSPACE_PACKAGE_JSON_MANIFESTS_CACHE[$cache_key]+set} ]] || {
                EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES=$((EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES + 1))
                return 1
            }
            EVOP_PROJECT_CONTEXT_CACHE_LOOKUP_RESULT="${EVOP_PROJECT_CONTEXT_WORKSPACE_PACKAGE_JSON_MANIFESTS_CACHE[$cache_key]}"
            found=1
            ;;
        EVOP_PROJECT_CONTEXT_WORKSPACE_SCRIPT_CACHE)
            [[ -n ${EVOP_PROJECT_CONTEXT_WORKSPACE_SCRIPT_CACHE[$cache_key]+set} ]] || {
                EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES=$((EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES + 1))
                return 1
            }
            EVOP_PROJECT_CONTEXT_CACHE_LOOKUP_RESULT="${EVOP_PROJECT_CONTEXT_WORKSPACE_SCRIPT_CACHE[$cache_key]}"
            found=1
            ;;
        EVOP_PROJECT_CONTEXT_AGENT_TOOL_SURFACES_CACHE)
            [[ -n ${EVOP_PROJECT_CONTEXT_AGENT_TOOL_SURFACES_CACHE[$cache_key]+set} ]] || {
                EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES=$((EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES + 1))
                return 1
            }
            EVOP_PROJECT_CONTEXT_CACHE_LOOKUP_RESULT="${EVOP_PROJECT_CONTEXT_AGENT_TOOL_SURFACES_CACHE[$cache_key]}"
            found=1
            ;;
        EVOP_PROJECT_CONTEXT_AGENT_SUPPORT_TOOLS_CACHE)
            [[ -n ${EVOP_PROJECT_CONTEXT_AGENT_SUPPORT_TOOLS_CACHE[$cache_key]+set} ]] || {
                EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES=$((EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES + 1))
                return 1
            }
            EVOP_PROJECT_CONTEXT_CACHE_LOOKUP_RESULT="${EVOP_PROJECT_CONTEXT_AGENT_SUPPORT_TOOLS_CACHE[$cache_key]}"
            found=1
            ;;
        EVOP_PROJECT_CONTEXT_COMMAND_AVAILABILITY_CACHE)
            [[ -n ${EVOP_PROJECT_CONTEXT_COMMAND_AVAILABILITY_CACHE[$cache_key]+set} ]] || {
                EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES=$((EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES + 1))
                return 1
            }
            EVOP_PROJECT_CONTEXT_CACHE_LOOKUP_RESULT="${EVOP_PROJECT_CONTEXT_COMMAND_AVAILABILITY_CACHE[$cache_key]}"
            found=1
            ;;
        *)
            EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES=$((EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES + 1))
            return 1
            ;;
    esac

    if (( found == 1 )); then
        EVOP_PROJECT_CONTEXT_FACTS_CACHE_HITS=$((EVOP_PROJECT_CONTEXT_FACTS_CACHE_HITS + 1))
        return 0
    fi

    EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES=$((EVOP_PROJECT_CONTEXT_FACTS_CACHE_MISSES + 1))
    return 1
}

